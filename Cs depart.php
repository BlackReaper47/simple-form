<h4 class="display-6">Overview</h4><br>
<p>The Department of Computer Science is proud of its excellent facilities and internationally qualified faculty members.</p><br>
<p>Computer Science is the methodical study of processes for problem-solving. The focus of computer science program at COMSATS Abbottabad is to develop the understanding of these processes and to program computers to efficiently process important tasks.</p><br>
<p>The department aims to equip its students with hands on knowledge and competitive skills so that they can shine as brilliant programmers, exceptional researchers, outstanding problems solvers and innovative designers. In the era with rapid technological advancements, the Computer Science department is committed to train its students with advanced aptitude required to manage the growing needs of science and technology.</p><br>
<p>The department is proud of providing quality and variety in computer equipment; which are available for students’ use. It provides modern computer facilities including a preparatory computer lab, a research lab and a graduate student lab. Various tools including multiple programming languages, databases, 3D graphics, parallel and distributed processing tools, networking and web development tools are available in our computer labs. All of the computing equipment have access to the Internet via CUI’s Internet connection.</p><br>
<p>Students may assist teaching faculty or plan further study in a variety of fields. This leads to interaction with departmental faculty, which is an important benefit that CUI offers. Additionally, students and faculty attend social events and international conferences like the Frontiers of Information Technology (FIT) which is regularly conducted every year.</p><br>
<p>Research activities are being conducted on a high priority within the department. This has resulted in national and international recognition for our faculty and students alike. A number of research papers of national and international repute have been published in leading journals and presented at conferences all over the world.  CUI has dedicated research groups working in the areas of Cloud and Grid Computing, Communication Systems, Wireless Networks, Image Processing, Artificial Intelligence, Natural Language Processing and Security Systems.</p>
