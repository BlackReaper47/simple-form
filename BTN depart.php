<h4 class="display-6">Overview</h4><br>
<p>The Department offers quality education in academic and research at all levels including BS, MS and PhD.  Department is equipped with state-of-the-art teaching and research laboratories facilitated with updated central university library. The Department is trying to equip the students with current and cutting-edge technologies in the field of Agricultural Biotechnology, Environmental Biotechnology, Health Biotechnology and Industrial Biotechnology under the guidance of highly qualified faculty.  The department is establishing effective collaborations with national, international universities and research institutes to link with scientific community for implementation of quality research for the benefit of human beings.</p><br>
<h5 class="display-6">Mission Statement:</h5>
<ul>
  <li>To explore the potential of biotechnology for improvement in food, environment, and quality of human health.</li>
</ul>
<br>
<h5 class="display-6">Visions:</h5>
<ul>
  <li>Focusing on long lasting issues related to food production and preservation such as biotic, abiotic stresses and the environmental issues including increasing pollution of water and soil.</li>
  <li>Animal models being utilized to investigate some of the human issues such as hereditary diseases in hopes to find a concrete breakthrough and a cure.</li>
  <li>Develop industrial ingredients along with process that scale up products at local and national level.</li>
</ul>
